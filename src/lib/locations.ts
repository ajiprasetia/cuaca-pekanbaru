// Get unique districts
export const getDistricts = (): string[] => {
  return [...new Set(LOCATIONS.map(loc => loc.district))].sort();
};

// Get villages by district
export const getVillagesByDistrict = (districtName: string): Location[] => {
  return LOCATIONS.filter(loc => loc.district === districtName)
    .sort((a, b) => a.name.localeCompare(b.name));
};

// Get village count by district
export const getVillageCount = (districtName: string): number => {
  return LOCATIONS.filter(loc => loc.district === districtName).length;
};

// Find location by code
export const findLocationByCode = (code: string): Location | undefined => {
  return LOCATIONS.find(loc => loc.code === code);
};